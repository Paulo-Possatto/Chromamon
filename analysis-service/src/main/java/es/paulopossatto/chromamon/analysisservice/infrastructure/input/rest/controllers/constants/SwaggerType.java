package es.paulopossatto.chromamon.analysisservice.infrastructure.input.rest.controllers.constants;

/** Constants for the parameter type in the Swagger. */
public class SwaggerType {

  /** The swagger param type for Strings. */
  public static final String STRING = "string";

  /** The swagger param type for non-integer numbers (long, double...). */
  public static final String NUMBER = "number";

  /** The swagger param type for Integers. */
  public static final String INTEGER = "integer";

  /** The swagger param type for Booleans. */
  public static final String BOOLEAN = "boolean";

  /** The swagger param type for Arrays (List, ArrayList, HashSet...). */
  public static final String ARRAY = "array";

  /** The swagger param type for objects. */
  public static final String OBJECT = "object";
}
